"use client";

import { cn } from "../../../Util/cn";
import { motion, stagger, useAnimate, useInView } from "framer-motion";
import { useEffect, useRef } from "react";

export const TypewriterEffect = ({
  words,
  className,
  cursorClassName,
}: {
  words: {
    text: string;
    className?: string;
  }[];
  className?: string;
  cursorClassName?: string;
}) => {
  // Split text inside of words into an array of characters
  const wordsArray = words.map((word) => {
    return {
      ...word,
      text: word.text.split(""),
    };
  });

  const [scope, animate] = useAnimate();
  const isInView = useInView(scope);
  const cursorRef = useRef<HTMLSpanElement>(null);

  useEffect(() => {
    if (isInView) {
      animate(
        "span.character",
        {
          display: "inline-block",
          opacity: 1,
          width: "fit-content",
        },
        {
          duration: 0.3,
          delay: stagger(0.1, { startDelay: 0.5 }),
          ease: "easeInOut",
        }
      );
    }
  }, [isInView, animate]);

  const renderWords = () => {
    return (
      <motion.div ref={scope} className="inline">
        {wordsArray.map((word, idx) => {
          return (
            <div key={`word-${idx}`} className="inline-block">
              {word.text.map((char, index) => (
                <motion.span
                  initial={{ opacity: 0 }}
                  key={`char-${index}`}
                  className={cn(
                    "character dark:text-white text-black",
                    word.className
                  )}
                  animate={{ opacity: 1 }}
                  transition={{ duration: 0.3, delay: idx * 0.5 + index * 0.1 }}
                  style={{ display: 'inline-block' }}
                >
                  {char}
                </motion.span>
              ))}
              &nbsp;
            </div>
          );
        })}
        <motion.span
          ref={cursorRef}
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{
            duration: 0.8,
            repeat: Infinity,
            repeatType: "reverse",
          }}
          className={cn(
            "inline-block rounded-sm w-[4px] h-4 md:h-6 lg:h-10 bg-blue-500",
            cursorClassName
          )}
        ></motion.span>
      </motion.div>
    );
  };

  return (
    <div
      className={cn(
        "text-2xl sm:text-xl md:text-3xl lg:text-5xl font-bold text-center",
        className
      )}
    >
      {renderWords()}
    </div>
  );
};

